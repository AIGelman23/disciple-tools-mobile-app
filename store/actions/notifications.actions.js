export const NOTIFICATIONS_BY_USER_RESPONSE = 'NOTIFICATIONS_BY_USER_RESPONSE';
export const NOTIFICATIONS_BY_USER_SUCCESS = 'NOTIFICATIONS_BY_USER_SUCCESS';
