export const NOTIFICATIONS_MARK_AS_UNREAD = 'NOTIFICATIONS_MARK_AS_UNREAD';
export const NOTIFICATIONS_MARK_AS_UNREAD_START = 'NOTIFICATIONS_MARK_AS_UNREAD_START';
export const NOTIFICATIONS_MARK_AS_UNREAD_RESPONSE = 'NOTIFICATIONS_MARK_AS_UNREAD_RESPONSE';
